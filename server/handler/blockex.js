/**
 * Get transactions and unspent transactions by address.
 * @param {Object} req The request object.
 * @param {Object} res The response object.
 */
const getAddress = async (req, res) => {
  try {
    const qry = { 'vout.address': req.params.hash };
    const txs = await TX.find(qry).sort({ blockHeight: -1 }).limit(1000);
    const utxo = await UTXO.find({ address: req.params.hash }).sort({ blockHeight: -1 });

    res.json({ txs, utxo });
  } catch(err) {
    console.log(err);
    res.status(500).send(err.message || err);
  }
};

/**
 * Get block by hash or height.
 * @param {Object} req The request object.
 * @param {Object} res The response object.
 */
const getBlock = async (req, res) => {
  try {
    const query = isNaN(req.params.hash)
      ? { hash: req.params.hash }
      : { height: req.params.hash };
    const block = await Block.findOne(query);
    if (!block) {
      res.status(404).send('Unable to find the block!');
      return;
    }

    const txs = await TX.find({ txId: { $in: block.txs }});

    res.json({ block, txs });
  } catch(err) {
    console.log(err);
    res.status(500).send(err.message || err);
  }
};

